package com.ravi.entity;

public class Things {
	private int tid;
	private String tname;
	private float tcost;
	public int getTid() {
		return tid;
	}
	public void setTid(int tid) {
		this.tid = tid;
	}
	public String getTname() {
		return tname;
	}
	public void setTname(String tname) {
		this.tname = tname;
	}
	public float getTcost() {
		return tcost;
	}
	public void setTcost(float tcost) {
		this.tcost = tcost;
	}
	

}
