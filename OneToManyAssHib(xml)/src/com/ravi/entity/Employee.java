package com.ravi.entity;

public class Employee {
	private int eno;
	private String ename;
	private float esal;
	public int getEno() {
		return eno;
	}
	public void setEno(int eno) {
		this.eno = eno;
	}
	public String getename() {
		return ename;
	}
	public void setename(String ename) {
		this.ename = ename;
	}
	public float getEsal() {
		return esal;
	}
	public void setEsal(float esal) {
		this.esal = esal;
	}
	

}
