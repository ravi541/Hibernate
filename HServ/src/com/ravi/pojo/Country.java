package com.ravi.pojo;

public class Country {
private int cid;
private String cname;
private String ccap;
public int getCid() {
	return cid;
}
public void setCid(int cid) {
	this.cid = cid;
}
public String getCname() {
	return cname;
}
public void setCname(String cname) {
	this.cname = cname;
}
public String getCcap() {
	return ccap;
}
public void setCcap(String ccap) {
	this.ccap = ccap;
}

}
